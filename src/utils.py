def normalization_of_requirement_sj(list_no_norm: object) -> object:
    """–£–¥–∞–ª—è–µ—Ç –Ω–µ –Ω—É–∂–Ω—ã–µ –Ω–∞–º —Å–∏–º–≤–æ–ª—ã –ù–∞–≤–µ—Ä–Ω–æ –µ—Å—Ç—å –¥—Ä—É–≥–æ–π —Å–ø–æ—Å–æ–± üòÄ"""

    for i in list_no_norm:
        a = str(i.requirement)
        b = a.replace('<b>', ' ')
        c = b.replace('</b>', ' ')
        d = c.replace('<ul>', ' ')
        e = d.replace('</ul>', ' ')
        f = e.replace('<li>', ' ')
        g = f.replace('</li>', ' ')
        h = g.replace('<p>', ' ')
        k = h.replace('</p>', ' ')
        l = k.replace('<br>', ' ')
        m = l.replace('<br />', '')
        n = m.replace('\n', '')
        i.requirement = n
    return list_no_norm


def average_salary(list_):
    """–í—ã—á–∏—Å–ª—è–µ—Ç —Å—Ä–µ–¥–Ω—é—é –ø–æ salary from –Ω–µ —Å—á–∏—Ç–∞–µ—Ç –µ—Å–ª–∏ –∑–Ω–∞—á–µ–Ω–∏–µ –æ—Ç: 0"""
    sums = list_[0].salary_from
    for i in list_[1:]:
        sums = i.salary_from + sums
    return sums / len(list_)




